let currentDate = new Date();
let dates = [];
let simpleDates = [];

for (let i = 0; i < 7; i++) {
    let pastDate = new Date(currentDate.getTime() - (i * 24 * 60 * 60 * 1000));
    let month = pastDate.getMonth() + 1;
    let date = pastDate.getDate();
    month = month < 10 ? '0' + month : month;
    date = date < 10 ? '0' + date : date;
    let formattedDate = pastDate.getFullYear() + '-' + month + '-' + date;
    dates.push(formattedDate);

    let simpleDate = month + '-' + date; // 날짜 형식 변경: 'm-dd'
    simpleDates.push(simpleDate);
}

dates.reverse();
simpleDates.reverse();

